namespace FitnessTracker;
/// <summary>
/// Enum Values can be Sedentary, LightlyActive, ModeratelyActive, VeryActive or ExtremelyActive.
/// </summary>  
public enum ActivityLevel : int
{
    Sedentary = 1,
    LightlyActive = 2,
    ModeratelyActive = 3,
    VeryActive = 4,
    ExtremelyActive = 5
}